        <h4 class="text-center"><b>Volunteer and Speaker for Medical Detection Dogs</b> </h4>
         <em>2010 to 2017</em>
         <p>
              In 2011 due to the loss of my hypo awareness (where people with type 1 diabetes, are frequently unable to notice when they have low blood glucose). Medical Detection Dogs are a charity that train canines to alert their owner to an impending hypo from a change in the owners body scent, which changes as the blood glucose levels begin to rise or fall. This partnership was life changing for me so I wanted to help raise awareness and much needed funds for the charity. 
         </p>
          <p>
              I would provide support on event stands to support the team. I advertised locally to the groups that invited speakers to their meetings and I would talk passionately about the charity as well as my personal situation and how having my Medical Alert Dog changed my life.
         </p>
